import Foundation
import UIKit

extension UIFont {
    
    static func figtreeRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont.init(name: "Figtree-Regular", size: size)!
    }
    
    static func figtreeSemiBold(ofSize size: CGFloat) -> UIFont {
        return UIFont.init(name: "Figtree-SemiBold", size: size)!
    }
}
